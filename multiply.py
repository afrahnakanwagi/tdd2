def multiply(a, b):
    return a * b

print(multiply(1, 1))  # Output: 1
print(multiply(2, 2))  # Output: 4
print(multiply(3, 3))  # Output: 9
print(multiply(4, 4))  # Output: 16

# Compare the result of multiply(23, 25) with 23*45
result = multiply(23, 25)
expected = 23 * 45
print(result == expected)  # This will print True or False
